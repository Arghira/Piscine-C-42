#include <unistd.h>

int	match(char *s1, char *s2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s1[i] != '\0' && s2[j] != '\0')
	{
		if (s2[j] == '*')
		{
			while (s2[j + 1] == '*')
				j++;
			while (s1[i] != '\0' && s1[i] != s2[j + 1])
				i++;
			j++;
		}
		if (s1[i] != s2[j])
			return (0);
		i++;
		j++;
	}
	return (1);
}
