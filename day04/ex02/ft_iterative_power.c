int	ft_iterative_power(int nb, int power)
{
	int i;
	int r;

	i = 1;
	r = 1;
	if (power < 0)
	{
		return (0);
	}
	if (power == 0)
	{
		return (1);
	}
	if (power > 0)
	{
		while (power >= 1)
		{
			r = r * nb;
			power--;
		}
		return (r);
	}
	return (0);
}
