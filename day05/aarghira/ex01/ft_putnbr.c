void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	if (nb >= 0)
		ft_putchar(nb + '0');
	else if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
		ft_putchar(nb + '0');
	}
}
