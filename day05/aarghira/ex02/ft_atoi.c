int	ft_atoi(char *str)
{
	int	i;
	int r;
	int s;

	r = 0;
	i = 0;
	s = 1;
	if (str[0] == '-')
	{
		s = -1;
		i++;
	}
	while (str[i] != '\0')
	{
		r = r * 10 + (str[i] - '0');
		i++;
	}
	r = r * s;
	return (r);
}
