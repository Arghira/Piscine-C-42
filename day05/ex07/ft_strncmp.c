int	ft_strncmp(char *s1, char *s2, unsigned n)
{
	int				i;
	unsigned int	k;

	k = 0;
	i = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && k < n)
	{
		if (s1[i] > s2[i])
			return (1);
		else if (s1[i] < s2[i])
			return (-1);
		else
			k++;
		i++;
	}
	return (0);
}
