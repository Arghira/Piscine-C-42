int	*ft_map(int *tab, int lenght, int (*f)(int))
{
	int	i;
	int *r;

	r = (int*)malloc(sizeof(int) * lenght);
	i = 0;
	while (i < lenght)
	{
		r[i] = (*f)(tab[i]);
		i++;
	}
	return (r);
}
